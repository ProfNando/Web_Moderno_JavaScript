const a = 7; // o sinal de igual '=' atribui e nao compara
let b = 3; 
b += a; // b = b + a (atribuição aditiva)
console.log(b);
b -= a; // b = b - a (atribuição subtrativa)
console.log(b);
b *= a; // b = b * a (atribuição multiplcativa)
console.log(b);
b /= a; // b = b / a (atribuição divsiva)
console.log(b);
b %= a; // b = b % a (atribuição modular)